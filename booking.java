    public void bioskoplist() {
        Object[] baris = {"Id Bioskop", "Lokasi"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_bioskop.setModel(tabmode);
        try {
            hasil = aa.dataBioskop();
            while (hasil.next()) {
                aa.setId_bioskop(hasil.getInt("id_bioskop"));
                aa.setLokasi(hasil.getString("lokasi"));
                String[] data = {String.valueOf(aa.getId_bioskop()), aa.getLokasi()};
                tabmode.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void schedulelist(int idBioskop, int idFilm) {
     Object[] baris = {"Id Show", "Tanggal", "Waktu", "Studio"};
     tabmode = new DefaultTableModel(null, baris);
     tabel_show.setModel(tabmode);
     try {
         hasil = aa.dataShow(idBioskop, idFilm);
         while (hasil.next()) {
             aa.setS_id_show(hasil.getInt("id_show"));
             aa.setJ_tanggal(hasil.getString("tanggal"));
             aa.setJ_waktu(hasil.getString("waktu"));
             aa.setJ_studio(hasil.getString("studio"));
             String[] data = {String.valueOf(aa.getS_id_show()), aa.getJ_tanggal(), aa.getJ_waktu(), aa.getJ_studio()};
             tabmode.addRow(data);
         }
     } catch (Exception e) {
         e.printStackTrace();
     }
   }
    

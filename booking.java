    private void bt_orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_orderActionPerformed
        // TODO add your handling code here
        // Mengambil nilai dari input pengguna
        int idShow = Integer.parseInt(txt_show.getText());
        int jmlSeat = Integer.parseInt(txt_jltiket.getText());
        String seat1 = txt_s1.getText().trim();
        String seat2 = txt_s2.getText().trim();
        String seat3 = txt_s3.getText().trim();
        String seat4 = txt_s4.getText().trim();

        // Validasi jumlah kursi yang diisi berdasarkan jumlah tiket
        if (jmlSeat < 1 || jmlSeat > 4) {
            JOptionPane.showMessageDialog(null, "Jumlah tiket harus antara 1 dan 4", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (jmlSeat >= 1 && seat1.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kursi 1 harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jmlSeat >= 2 && seat2.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kursi 2 harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jmlSeat >= 3 && seat3.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kursi 3 harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (jmlSeat == 4 && seat4.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Kursi 4 harus diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Memanggil metode updateBooking
        aa.updateBooking(bookingId, idShow, jmlSeat, seat1, seat2, seat3, seat4);

        // Menampilkan dialog untuk pesan makanan atau metode pembayaran
        if (JOptionPane.showConfirmDialog(null, "Apakah anda ingin pesan makan?", "Warning", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            // Jika pengguna memilih "Yes"
            try {
                new fnb(userId, bookingId).setVisible(true);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal masuk ke halaman pesan makan", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            // Jika pengguna memilih "No"
            try {
               konfirmOrder KO = new konfirmOrder(userId,bookingId);
               KO.setVisible(true);
               this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal masuk ke halaman konfirmasi pembayaran", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_bt_orderActionPerformed

